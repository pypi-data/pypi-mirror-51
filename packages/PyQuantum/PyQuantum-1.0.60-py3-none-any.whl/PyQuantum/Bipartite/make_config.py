def make_config(capacity, n, wc, wa, g, T, nt, filename='./src/Bipartite/config2.py'):
    with open(filename, 'w') as f:
        f.write('# --------------------' + '\n')
        f.write('KHz = 10 ** 3  # 1 KГц' + '\n')
        f.write('MHz = 10 ** 6  # 1 МГц' + '\n')
        f.write('GHz = 10 ** 9  # 1 ГГц' + '\n')
        f.write('\n')
        f.write('mks = 1e-6  # 1 мкс' + '\n')
        f.write('ns = 1e-9  # 1 мкс' + '\n')
        f.write('\n')
        f.write('# BEGIN----------------------------------------- CAVITY -------------------------------------------' + '\n')
        f.write('capacity = ' + str(capacity) + '\n')
        f.write('n = ' + str(n) + '\n')
        f.write('\n')
        f.write('wc = ' + str(wc) + ' * GHz' + '\n')
        f.write('wa = ' + str(wa) + ' * GHz' + '\n')
        f.write('\n')
        f.write('g = ' + str(g) + ' * GHz' + '\n')
        f.write('# END------------------------------------------- CAVITY -------------------------------------------' + '\n')
        f.write('\n')
        f.write('# -------------------------------------------------------------------------------------------------' + '\n')
        f.write('T = ' + str(T) + ' * mks' + '\n')
        f.write('\n')
        f.write('nt = ' + str(nt) + '\n')
        f.write('# -------------------------------------------------------------------------------------------------' + '\n')
        f.write('\n')
        f.write('init_state = [0, n]' + '\n')
        f.write('\n')
        f.write('# -------------------------------------------------------------------------------------------------' + '\n')
        f.write('\n')
        f.write('precision = 5' + '\n')
        f.write('\n')
        f.write('# -------------------------------------------------------------------------------------------------' + '\n')
        f.write('path = "Bipartite/out/" + str(capacity) + "_" + str(n) + "/"' + '\n')
        f.write('H_html = path + "H.html"' + '\n')
        f.write('H_csv = path + "H.csv"' + '\n')
        f.write('U_csv = path + "U.csv"' + '\n')
        f.write('ro_0_csv = path + "ro_0.csv"' + '\n')
        f.write('U_csv = path + "U.csv"' + '\n')
        f.write('x_csv = path + "x.csv"' + '\n')
        f.write('y_csv = path + "t.csv"' + '\n')
        f.write('z_csv = path + "z.csv"' + '\n')
        f.write('z_all_csv = path + "z_all.csv"' + '\n')
        f.write('fid_csv = path + "fid.csv"' + '\n')
        f.write('fid_small_csv = path + "fid_small.csv"' + '\n')
        f.write('# -------------------------------------------------------------------------------------------------' + '\n')

    return 1
